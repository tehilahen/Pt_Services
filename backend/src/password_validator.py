"""
ולידציה של סיסמאות - בודק שסיסמאות עומדות בדרישות אבטחה
דף זה אחראי על:
- בדיקת חוזק סיסמאות (אורך, אותיות גדולות/קטנות, ספרות, תווים מיוחדים)
- החזרת הודעות שגיאה ברורות בעברית
- רשימת דרישות סיסמה למשתמש
- שימוש ב-regex לבדיקות מתקדמות
"""
import re

def validate_password_strength(password):
    """
    בודק שהסיסמה עומדת בדרישות:
    - לפחות 8 תווים
    - לפחות אות גדולה אחת באנגלית
    - לפחות אות קטנה אחת באנגלית
    - לפחות ספרה אחת
    - לפחות תו מיוחד אחד
    
    Returns:
        tuple: (is_valid: bool, message: str)
    """
    if not password or len(password) < 8:
        return False, "הסיסמה חייבת להכיל לפחות 8 תווים"
    
    if not re.search(r'[A-Z]', password):
        return False, "הסיסמה חייבת להכיל לפחות אות גדולה אחת באנגלית (A-Z)"
    
    if not re.search(r'[a-z]', password):
        return False, "הסיסמה חייבת להכיל לפחות אות קטנה אחת באנגלית (a-z)"
    
    if not re.search(r'[0-9]', password):
        return False, "הסיסמה חייבת להכיל לפחות ספרה אחת (0-9)"
    
    if not re.search(r'[!@#$%^&*(),.?":{}|<>_\-+=\[\]\\\/]', password):
        return False, "הסיסמה חייבת להכיל לפחות תו מיוחד אחד (!@#$%^&* וכו')"
    
    return True, "הסיסמה עומדת בדרישות"

def get_password_requirements():
    """
    מחזיר את דרישות הסיסמה כרשימה
    """
    return [
        "לפחות 8 תווים",
        "אות גדולה באנגלית (A-Z)",
        "אות קטנה באנגלית (a-z)",
        "ספרה (0-9)",
        "תו מיוחד (!@#$%^&* וכו')"
    ]

